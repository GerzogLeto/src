public class CompanyData {
    private String nameRubrika;
    private String nameCompany;
    private String urlLink;
    private String adress;
    private String aboutCompany;
    private String[] numberPhones;

    public String getNameRubrika() {
        return nameRubrika;
    }

    public void setNameRubrika(String nameRubrika) {
        this.nameRubrika = nameRubrika;
    }

    public String getNameCompany() {
        return nameCompany;
    }

    public void setNameCompany(String nameCompany) {
        this.nameCompany = nameCompany;
    }

    public String getUrlLink() {
        return urlLink;
    }

    public void setUrlLink(String urlLink) {
        this.urlLink = urlLink;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public String getAboutCompany() {
        return aboutCompany;
    }

    public void setAboutCompany(String aboutCompany) {
        this.aboutCompany = aboutCompany;
    }

    public String[] getNumberPhones() {
        return numberPhones;
    }

    public void setNumberPhones(String[] numberPhones) {
        this.numberPhones = numberPhones;
    }
}
